<?php

namespace Database\Seeders;

use App\Models\Book;
use Illuminate\Database\Seeder;

class BooksAndChaptersSeeder extends Seeder
{
    public function run(): void
    {
        $this->command->info('🚀 بدء إدخال الكتب والأبواب...');

        $books = $this->getData();
        $bookCount = 0;
        $chapterCount = 0;

        foreach ($books as $i => $bookData) {
            $mainBook = Book::updateOrCreate(
                ['name' => $bookData['name'], 'parent_id' => null],
                ['sort_order' => $i + 1]
            );
            $bookCount++;

            if (!empty($bookData['chapters'])) {
                foreach ($bookData['chapters'] as $j => $chapterName) {
                    Book::updateOrCreate(
                        ['name' => $chapterName, 'parent_id' => $mainBook->id],
                        ['sort_order' => $j + 1]
                    );
                    $chapterCount++;
                }
            }
        }

        $this->command->info("✅ تم إدخال {$bookCount} كتاب و {$chapterCount} باب بنجاح!");
    }

