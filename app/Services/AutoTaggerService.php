<?php

namespace App\Services;

use App\Models\Hadith;
use Illuminate\Support\Str;

class AutoTaggerService
{
    /**
     * القاموس الدلالي لربط الكلمات المفتاحية بالمواضيع
     * Dictionary mapping keywords to SEO-friendly topics
     *
     * @var array<string, array<string>>
     */
    private array $dictionary = [
        'بر الوالدين' => ['والد', 'والدة', 'أمك', 'أبيك', 'عقوق', 'أمه', 'أباه', 'بر والديه', 'طاعة الوالدين'],
        'الصبر على البلاء' => ['صبر', 'يصبر', 'ابتلاء', 'بلاء', 'مصيبة', 'احتسب', 'جزع'],
        'فضل الصلاة' => ['صلاة', 'يصلي', 'ركعة', 'سجود', 'مسجد', 'وضوء', 'أذان'],
        'الصدقة والزكاة' => ['صدق', 'تصدق', 'زكاة', 'ينفق', 'يتصدق', 'مسكين', 'فقير'],
        'فضل القرآن' => ['قرآن', 'يتلو', 'سورة', 'آية', 'تلاوة', 'مصحف'],
        'التوبة والاستغفار' => ['توب', 'يتوب', 'استغفر', 'مغفرة', 'عفو', 'رحمة'],
        'يوم القيامة' => ['قيامة', 'حشر', 'حساب', 'ميزان', 'صراط', 'بعث'],
        'الجنة والنار' => ['جنة', 'نار', 'جهنم', 'فردوس', 'نعيم', 'عذاب'],
        'الأخلاق الحسنة' => ['خلق', 'صدق', 'أمانة', 'حياء', 'عفو', 'رحمة', 'كرم'],
        'شهر رمضان' => ['رمضان', 'صيام', 'صوم', 'صائم', 'تراويح', 'ليلة القدر'],
        'الحج والعمرة' => ['حج', 'عمرة', 'طواف', 'سعي', 'عرفات', 'مكة', 'كعبة'],
        'الزواج والأسرة' => ['نكاح', 'زواج', 'زوج', 'زوجة', 'مهر', 'أسرة', 'بنت', 'ولد'],
        'الأذكار والدعاء' => ['ذكر', 'دعاء', 'يدعو', 'سبحان', 'حمد', 'تكبير', 'تهليل'],
        'الجهاد والشهادة' => ['جهاد', 'شهيد', 'غزوة', 'سيف', 'رباط', 'قتال'],
        'حسن الظن بالله' => ['ظن', 'يتوكل', 'توكل', 'يقين', 'رجاء'],
        'حفظ اللسان' => ['لسان', 'كذب', 'غيبة', 'نميمة', 'سب', 'شتم'],
    ];

    /**
     * توليد وسوم للحديث بناءً على محتواه
     *
     * @param Hadith $hadith
     * @return void
     */
    public function tagHadith(Hadith $hadith): void
    {
        // استخدام النص المجرد من التشكيل لضمان دقة البحث
        $textToAnalyze = $hadith->content_searchable ?? $hadith->content;
        
        // إذا لم يكن هناك نص، نخرج مبكراً
        if (empty($textToAnalyze)) {
            return;
        }

        $matchedTags = [];

        foreach ($this->dictionary as $topic => $keywords) {
            foreach ($keywords as $keyword) {
                // نبحث عن النص ككلمة كاملة أو جزء منها
                if (mb_strpos($textToAnalyze, $keyword) !== false) {
                    $matchedTags[] = $topic;
                    break; // ننتقل للموضوع التالي بمجرد إيجاد أول كلمة مفتاحية له
                }
            }
        }

        // إذا تم العثور على مواضيع، نربطها بالحديث
        if (!empty($matchedTags)) {
            // نستخدم attachTags بدلاً من syncTags لعدم مسح أي وسوم يدوية قد يضيفها المحرر
            $hadith->attachTags($matchedTags);
        }
    }
}
