<?php
require 'vendor/autoload.php';
$app = require_once 'bootstrap/app.php';
$app->make('Illuminate\Contracts\Console\Kernel')->bootstrap();

$parser = new \App\Services\HadithParser();

$text = 'أَتَانِي رَبِّي اللَّيْلَةَ فِي أَحْسَنِ صُورَةٍ فَقَالَ: يَا مُحَمَّدُ، هَلْ تَدْرِي فِيمَ يَخْتَصِمُ الْمَلَأُ الْأَعْلَى؟ قُلْتُ: لَا. فَوَضَعَ يَدَهُ بَيْنَ كَتِفَيَّ فَوَجَدْتُ بَرْدَهَا بَيْنَ ثَدْيَيَّ فَعَلِمْتُ مَا فِي السَّمَوَاتِ وَالْأَرْضِ، ثُمَّ قَالَ: يَا مُحَمَّدُ، هَلْ تَدْرِي فِيمَ يَخْتَصِمُ الْمَلَأُ الْأَعْلَى؟ قُلْتُ: نَعَمْ، يَخْتَصِمُونَ فِي الْكَفَّارَاتِ، وَالدَّرَجَاتِ، فَالْكَفَّارَاتُ الْمُكْثُ فِي الْمَسَاجِدِ يَعْنِي بَعْدَ الصَّلَوَاتِ، وَالْمَشْيُ عَلَى الْأَقْدَامِ إِلَى الْجَمَاعَاتِ، وَإِسْبَاغُ الْوضُوءِ فِي الْمَكَارِهِ، وَانْتِظَارُ الصَّلَاةِ بَعْدَ الصَّلَاةِ، وَمَنْ فَعَلَ ذَلِكَ عَاشَ بِخَيْرٍ وَمَاتَ بِخَيْرٍ، وَكَانَ مِنْ ذُنُوبِهِ كَيَوْمِ وَلَدَتْهُ أُمُّهُ، وَقَالَ: يَا مُحَمَّدُ، إِذَا صَلَّيْتَ فَقُلْ: اللَّهُمَّ إِنِّي أَسْأَلُكَ الْخَيْرَاتِ وَتَرْكَ الْمُنْكَرَاتِ، وَحُبَّ الْمَسَاكِينِ، (وَأَنْ تَغْفِرَ لِي وَتَرْحَمَنِي وَتَتُوبَ عَلَيَّ،) وَإِذَا أَرَدْتَ بِقَوْمٍ فِتْنَةً أَنْ تَقْبِضَنِي إِلَيْكَ غَيْرَ مَفْتُونٍ، وَالدَّرَجَاتُ: بَذْلُ الطَّعَامِ وَإِفْشَاءُ السَّلَامِ وَالصَّلَاةُ بِاللَّيْلِ وَالنَّاسُ نِيَامٌ. [59] (صحيح) (عب حم عبد بن حميد ت) عن ابن عباس. (وما بين القوسين زيادة من الآحاد والمثاني لابن أبي عاصم.';

$result = $parser->parse($text);

echo "=== ADDITIONS ===\n";
echo json_encode($result['additions'], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT) . "\n\n";

echo "=== CHAINS ===\n";
echo json_encode($result['chains'], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT) . "\n\n";

echo "=== SOURCE CODES ===\n";
echo json_encode($result['source_codes'], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT) . "\n\n";

echo "=== NARRATORS ===\n";
echo json_encode($result['narrators'], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT) . "\n";
